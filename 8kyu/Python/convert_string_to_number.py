#!/usr/bin/env python3

def string_to_number(s):
    n=int(s)
    return n

str1="1234"
str2="605"
str3="1405"
str4="-7"

print(f"{str1} is {string_to_number(str1)}")
print(f"{str2} is {string_to_number(str2)}")
print(f"{str3} is {string_to_number(str3)}")
print(f"{str4} is {string_to_number(str4)}")
